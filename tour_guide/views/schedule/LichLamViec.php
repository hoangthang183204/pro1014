// T·∫°o l·ªãch th√°ng
function generateCalendar(year, month) {
    const calendarBody = document.getElementById('calendarBody');
    calendarBody.innerHTML = '';
    
    // Ng√†y ƒë·∫ßu ti√™n c·ªßa th√°ng
    const firstDay = new Date(year, month - 1, 1);
    // Ng√†y cu·ªëi c√πng c·ªßa th√°ng
    const lastDay = new Date(year, month, 0);
    // Ng√†y ƒë·∫ßu ti√™n trong tu·∫ßn (0 = Ch·ªß nh·∫≠t, 1 = Th·ª© 2)
    let startDay = firstDay.getDay();
    // Chuy·ªÉn t·ª´ Ch·ªß nh·∫≠t = 0 sang Th·ª© 2 = 0
    startDay = startDay === 0 ? 6 : startDay - 1;
    
    // T·ªïng s·ªë ng√†y trong th√°ng
    const daysInMonth = lastDay.getDate();
    
    // T√≠nh to√°n s·ªë tu·∫ßn
    const totalCells = startDay + daysInMonth;
    const weeks = Math.ceil(totalCells / 7);
    
    let dayCounter = 1;
    let currentWeek = 0;
    
    for (let week = 0; week < weeks; week++) {
        const row = document.createElement('tr');
        
        for (let dayOfWeek = 0; dayOfWeek < 7; dayOfWeek++) {
            const cell = document.createElement('td');
            cell.className = 'calendar-day';
            
            // T√≠nh ng√†y th√°ng cho √¥ hi·ªán t·∫°i
            const cellIndex = (week * 7) + dayOfWeek;
            
            if (cellIndex < startDay || dayCounter > daysInMonth) {
                // √î tr·ªëng (th√°ng tr∆∞·ªõc ho·∫∑c th√°ng sau)
                cell.className += ' other-month';
                
                // T√≠nh ng√†y c·ªßa th√°ng tr∆∞·ªõc cho c√°c √¥ ƒë·∫ßu
                if (cellIndex < startDay) {
                    // T√≠nh s·ªë ng√†y c·ªßa th√°ng tr∆∞·ªõc
                    const prevMonth = month - 1 === 0 ? 12 : month - 1;
                    const prevYear = month - 1 === 0 ? year - 1 : year;
                    const prevMonthLastDay = new Date(prevYear, prevMonth, 0).getDate();
                    const prevMonthDay = prevMonthLastDay - (startDay - cellIndex) + 1;
                    cell.innerHTML = `<div class="calendar-date">${prevMonthDay}</div>`;
                } else {
                    // T√≠nh ng√†y c·ªßa th√°ng sau cho c√°c √¥ cu·ªëi
                    const nextMonthDay = cellIndex - startDay - daysInMonth + 1;
                    cell.innerHTML = `<div class="calendar-date">${nextMonthDay}</div>`;
                }
            } else {
                // Ng√†y trong th√°ng
                const currentDate = new Date(year, month - 1, dayCounter);
                const dateStr = currentDate.toISOString().split('T')[0];
                const isToday = isSameDay(currentDate, new Date());
                
                if (isToday) {
                    cell.className += ' today';
                }
                
                let content = `<div class="calendar-date">${dayCounter}</div>`;
                
                // Hi·ªÉn th·ªã s·ª± ki·ªán trong ng√†y
                const events = getEventsForDate(dateStr);
                events.forEach(event => {
                    let eventClass = '';
                    switch(event.type) {
                        case 'tour': eventClass = 'tour-event'; break;
                        case 'busy': eventClass = 'busy-event'; break;
                        case 'off': eventClass = 'off-event'; break;
                    }
                    content += `<span class="${eventClass}">${event.title}</span>`;
                });
                
                cell.innerHTML = content;
                cell.dataset.date = dateStr;
                cell.addEventListener('click', () => showDayDetail(dateStr));
                
                dayCounter++;
            }
            
            row.appendChild(cell);
        }
        
        calendarBody.appendChild(row);
        currentWeek++;
    }
}

// L·∫•y s·ª± ki·ªán cho m·ªôt ng√†y c·ª• th·ªÉ
function getEventsForDate(dateStr) {
    const events = [];
    
    // Ki·ªÉm tra l·ªãch l√†m vi·ªác
    scheduleData.forEach(item => {
        if (item.ngay === dateStr) {
            events.push({
                type: getEventType(item.loai_lich),
                title: getEventTitle(item.loai_lich, item.ghi_chu)
            });
        }
    });
    
    // Ki·ªÉm tra tour
    tourData.forEach(tour => {
        try {
            const tourStart = new Date(tour.ngay_bat_dau);
            const tourEnd = new Date(tour.ngay_ket_thuc);
            const currentDate = new Date(dateStr);
            
            // ƒê·∫∑t gi·ªù v·ªÅ 0 ƒë·ªÉ so s√°nh ch·ªâ ng√†y
            tourStart.setHours(0, 0, 0, 0);
            tourEnd.setHours(0, 0, 0, 0);
            currentDate.setHours(0, 0, 0, 0);
            
            if (currentDate >= tourStart && currentDate <= tourEnd) {
                events.push({
                    type: 'tour',
                    title: tour.ten_tour
                });
            }
        } catch (e) {
            console.error('L·ªói x·ª≠ l√Ω ng√†y tour:', e);
        }
    });
    
    return events;
}

// Chuy·ªÉn lo·∫°i l·ªãch th√†nh lo·∫°i s·ª± ki·ªán
function getEventType(loaiLich) {
    switch(loaiLich) {
        case 'ƒë√£ ph√¢n c√¥ng': return 'tour';
        case 'b·∫≠n': return 'busy';
        case 'ngh·ªâ': return 'off';
        default: return 'busy';
    }
}

// T·∫°o ti√™u ƒë·ªÅ s·ª± ki·ªán
function getEventTitle(loaiLich, ghiChu) {
    const titles = {
        'ƒë√£ ph√¢n c√¥ng': 'C√≥ tour',
        'b·∫≠n': 'B·∫≠n',
        'ngh·ªâ': 'Ngh·ªâ',
        'c√≥ th·ªÉ l√†m': 'C√≥ th·ªÉ l√†m'
    };
    
    let title = titles[loaiLich] || loaiLich;
    if (ghiChu && ghiChu.length > 0) {
        title += ': ' + ghiChu.substring(0, 20);
    }
    
    return title;
}

// So s√°nh 2 ng√†y
function isSameDay(date1, date2) {
    return date1.getFullYear() === date2.getFullYear() &&
           date1.getMonth() === date2.getMonth() &&
           date1.getDate() === date2.getDate();
}

// Hi·ªÉn th·ªã chi ti·∫øt ng√†y
function showDayDetail(dateStr) {
    const date = new Date(dateStr);
    const formattedDate = date.toLocaleDateString('vi-VN', {
        weekday: 'long',
        year: 'numeric',
        month: 'long',
        day: 'numeric'
    });
    
    // L·∫•y s·ª± ki·ªán trong ng√†y
    const events = getEventsForDate(dateStr);
    
    let content = `<h5>${formattedDate}</h5>`;
    
    if (events.length === 0) {
        content += '<p class="text-muted">Kh√¥ng c√≥ s·ª± ki·ªán n√†o</p>';
    } else {
        content += '<ul class="list-group">';
        events.forEach(event => {
            content += `<li class="list-group-item">${event.title}</li>`;
        });
        content += '</ul>';
    }
    
    // Hi·ªÉn th·ªã modal
    const modal = new bootstrap.Modal(document.getElementById('dayDetailModal'));
    document.getElementById('modalTitle').innerHTML = formattedDate;
    document.getElementById('modalBody').innerHTML = content;
    modal.show();
}

